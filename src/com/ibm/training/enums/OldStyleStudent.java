package com.ibm.training.enums;

public class OldStyleStudent {
	
	int id;
	String name;
	char grade; //Valid grades are A,B,C,D
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public char getGrade() {
		return grade;
	}
	public void setGrade(char grade) {
		this.grade = grade;
	}
	
	

}
